                if ($board[$i][$j] == 'R') {
                    $ROOK = ['row' => $i, 'col' => $j];
                }
            }
        }

        $count = 0;
        for ($i=0; $i<8; $i++) {

            $board[$i][$ROOK['col']];
        }

        return $count;
    }
